    // MARK: - NFC commands (Abbott custom ISO-15693 manufacturer range 0xA0..0xDF)
    //
    // These are NOT standard ISO 15693 commands. `0x20` is standard read;
    // `0xA0..0xDF` is reserved for vendor commands and Abbott uses it for
    // Libre family operations.

    /// `02 A1 7A` — Get PatchInfo. Pure read; no payload. Returns metadata
    /// describing sensor type / generation.
    static let nfcCommandGetPatchInfo = Data([0x02, 0xA1, 0x7A])

    /// `02 A0 7A` — Activate. Followed by 10-byte payload
    /// `time(4 LE) || receiverId(4 LE) || CRC16(2)`. Used for FRESH
    /// sensor activation. In this app we DON'T do activation — the
    /// Libre 3 app handles activation. We only do takeover.
    static let nfcCommandActivationPrefix = Data([0x02, 0xA0, 0x7A])

    /// `02 A8 7A` — Transfer / Take-over. Same payload shape as activation.
    /// Used when an already-activated sensor (currently bound to another
    /// phone such as the Libre 3 app) is being adopted by this app.
    /// **This is the primary NFC path for this server-backed architecture.**
    static let nfcCommandTransferPrefix = Data([0x02, 0xA8, 0x7A])

    /// Per the corrected understanding (DiaBLE confirms this), the
    /// 10-byte payload is `time(4 LE) || receiverId(4 LE) || CRC16(2)`.
    /// No `libcrl_dp.so` involvement — it's a plain CRC16.
    static let nfcPayloadLength = 10

    /// Response shape for A0/A8: 16 bytes (after stripping any leading
    /// `0xA5` padding) = `bdAddress(6 reversed) || BLE_PIN(4) || activationTime(4 LE) || CRC16(2)`.
    static let nfcResponseLength = 16

