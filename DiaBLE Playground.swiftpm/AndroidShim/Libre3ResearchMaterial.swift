        /// `0x09` — request patch certificate. Juggluco: `sendSecurityCommand(9)`.
        static let requestPatchCertificate: UInt8 = 0x09
        /// `0x0D` — sendAppEphemeral (= decimal 13). Juggluco: `sendSecurityCommand(0x0D)`.
        static let sendAppEphemeral: UInt8 = 0x0D
        /// `0x0E` — app-ephemeral chunks done (= decimal 14). Juggluco: `sendSecurityCommand(14)`.
        static let appEphemeralChunksDone: UInt8 = 0x0E
        /// `0x11` — request challenge / preauthorized (= **decimal 17**).
        /// Juggluco: `sendSecurityCommand(17)` — the SKB doc's "17" is decimal,
        /// not hex. Same byte for fresh and cached paths.
