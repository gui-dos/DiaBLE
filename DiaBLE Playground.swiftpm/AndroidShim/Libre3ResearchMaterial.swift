    enum SecurityCommand {
        /// `0x01` — startAuth. Juggluco: `sendSecurityCommand(1)`.
        static let startAuth: UInt8 = 0x01
        /// `0x02` — sendAppCertificate. Juggluco: `sendSecurityCommand(2)`.
